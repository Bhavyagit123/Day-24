package com.codegnan.oop;

public class Donation {
	private int donorId;
    private int amount;

    public void setDonorId(int donorId) {
        this.donorId = donorId;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }

    public int getDonorId() {
        return donorId;
    }

    public int getAmount() {
        return amount;
    }
}


